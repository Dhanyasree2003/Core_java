package Corejava.operators;

public class increment_decrement {
    public static void main(String[] args) {
        int val1=159;
        int val2=160;
        int val3=350;
        int val4=500;
        System.out.println(++val1);
        System.out.println(--val2);
        System.out.println(val3++);
        System.out.println(val4--);
        System.out.println(++val3);
        System.out.println(--val4);
    }
}
